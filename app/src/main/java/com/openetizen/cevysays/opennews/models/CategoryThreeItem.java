public class CategoryThreeItem {
    private String title;
    private String content;
    private String pict_content;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getPict_content() {
        return pict_content;
    }

    public void setPict_content(String pict_content) {
        this.pict_content = pict_content;
    }
}
